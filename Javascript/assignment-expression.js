// Assignment Expression
let firstNumber = 10;
let secondNumber = 5;
// Addition '+='
firstNumber += secondNumber;
console.log("First Number:", firstNumber);
// Subtraction '-='
firstNumber -= secondNumber;
console.log("Subtraction :", firstNumber);
// Multiplication '*='
firstNumber *= secondNumber;
console.log("Multiplication :", firstNumber);
// Division '/='
firstNumber /= secondNumber;
console.log("Division :", firstNumber);