//For loop
for  (let i = 1; i <= 5; i++) {
    console.log('Count:', i);
}


// While loop
let count = 1;
while (count <= 5) {
    console.log('Processing...')
    count++
    console.log('While count:', count);
}

// do-while loop
let counter = 1;
do {
    console.log('Do-while count:', counter)
    counter++
} while (counter <= 5);